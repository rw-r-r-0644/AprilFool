// Decrypt and print the text
void print_enc_text(u16 * enc_text)
{
	char * key_w = malloc(14); // The key gets edited during the decryption call
	strcpy(key_w, enc_key);
	char * text = decrypt_buffer(enc_text, key_w);
	tty_printf(text);
	free(key_w);
	free(text);
}

int vpad_error;
VPADData vpad_data;

int success = 0;

int curl_written = 0;
size_t write_data(void *ptr, size_t size, size_t nmemb, char * buf)
{
	memcpy(&buf[curl_written], ptr, size * nmemb);
	curl_written += (size * nmemb);
    return (size * nmemb);
}

int get_file(const char * url, char * buf)
{
	CURL * curl = n_curl_easy_init();
	if (curl)
	{
		n_curl_easy_setopt(curl, CURLOPT_URL, url);
		n_curl_easy_setopt(curl, CURLOPT_WRITEFUNCTION, write_data);
		n_curl_easy_setopt(curl, CURLOPT_WRITEDATA, buf);
		
		n_curl_easy_perform(curl);
		n_curl_easy_cleanup(curl); // always cleanup
		int ret = curl_written;
		curl_written = 0;
		return ret;
	}
	return 0;
}

int menu_main()
{
    memory_init();
	
	tty_init();
	
	tty_clear(0xFF000000);
	tty_flip();
	tty_clear(0xFF000000);
	tty_flip();
	
	// We don't need to encrypt apparently legit strings
	tty_printf("boot0-hax v%d rel %s", 139, "01042017\n");
	tty_printf("Press A to try dumping the boot1 key");
	tty_printf("Press B to exit");

	while (1)
	{
		VPADRead(0, &vpad_data, 1, &vpad_error);
		if (vpad_data.btns_h & VPAD_BUTTON_A)
			break;
		if (vpad_data.btns_h & VPAD_BUTTON_B)
		{
			// Exit like a legit application
			tty_end();
			memory_end();
			return -1;
		}
	}
	
	/* Download "yes" or "no" from pastebin, to check if we should show "race attack failed" or "REGGIE!!" */
	socket_lib_init();
	n_curl_global_init(CURL_GLOBAL_ALL); 
	
	// http://pastebin.com/raw/8Hquj60c
	u16 url_enc[34] = {0xE0, 0xD6, 0xDE, 0xE5, 0x5B, 0x91, 0x50, 0xBE, 0xCB, 0xE2, 0xEA, 0xDA, 0xC8, 0xE1, 0xD0, 0x98, 0xD8, 0x90, 0xCF, 0x50, 0xC0, 0xCB, 0xE6, 0xA5, 0xAD, 0xAE, 0xE9, 0xD7, 0xD4, 0xAB, 0x51, 0xC5, 0x00};
	
	char * key_w = malloc(14);
	strcpy(key_w, enc_key);
	char * url = decrypt_buffer(url_enc, key_w);
	free(key_w);
		
	char * buf = malloc(40);
	if (get_file(url, buf) > 0)
	{
		if (strcmp(buf, "no") == 0)
			success = 0;
		else if (strcmp(buf, "yes") == 0)
			success = 1;
		else
			success = 0;
	} // There's no internet or somtehing, don't show reggie yet
	else
		success = 0;
	
	free(buf);
	free(url);
	
	// IOSU RED
	tty_clear(0xFF000000);
	tty_flip();
	tty_clear(0xFF000000);
	tty_flip();
	
	// Print the messages and sleep
	for(u32 i = 0; i < (sizeof(print_text) / sizeof(print_text[0])); i++)
	{
		print_enc_text((u16 *)print_text[i].enc_text);
		msleep(print_text[i].delay);
		msleep(30); // Always wait 30ms to make a terminal-like effect
	}
	
	
	// Show Reggie!
	if(success)
	{
		for(u32 i = 0; i < (sizeof(print_text_success) / sizeof(print_text_success[0])); i++)
		{
			print_enc_text((u16 *)print_text_success[i].enc_text);
			msleep(print_text_success[i].delay);
			msleep(30);
		}
		
		tty_printf("Press A to exit to home menu");
		while (1)
		{
			VPADRead(0, &vpad_data, 1, &vpad_error);
			if (vpad_data.btns_h & VPAD_BUTTON_A)
				break;
		}
		
		tty_end();
		memory_end();
		
		return 0;
	}
	else // Crash
	{
		for(u32 i = 0; i < (sizeof(print_text_fail) / sizeof(print_text_fail[0])); i++)
		{
			print_enc_text((u16 *)print_text_fail[i].enc_text);
			msleep(print_text_fail[i].delay);
			msleep(30);
		}
		crash_with_sound();
	}
	
	

	//find_buffers(); // That was used for finding the second OSScreen buffer offset, so we can now directly write to the buffers!

	return 0;
}
	
